       public void Show_jTable(){
    int count=0;
    float T_Amount=0;
        ArrayList<User> list = getuserList();
        DefaultTableModel model =(DefaultTableModel)jTable1.getModel();
        Object [] row = new Object[5];
        for (int i=0; i<list.size(); i++){
            row[0]=list.get(i).getProduct_Name();
            row[1]=list.get(i).getProduct_Code();
            
            row[2]=list.get(i).getEXP_Date();
            row[3]=list.get(i).getQty();
            row[4]=list.get(i).getSelling_Price1();
            
            count++;
            T_Amount=T_Amount+list.get(i).getSelling_Price1();
            model.addRow(row);
            
           } 
        jLabel6_no_ofProducts.setText(Integer.toString(count));
        jLabel7_value.setText(""+T_Amount);
        
    }
    
    
    
    
    
