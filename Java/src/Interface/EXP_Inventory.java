       public ArrayList <User> getuserList(){
    
        ArrayList<User> userList = new ArrayList<User>();
        Connection con = DBconnect.connectdb();
        
        Statement st;
        ResultSet rs;
        
        try {
          
         String today = new SimpleDateFormat("yyyy-MM-dd").format(Calendar.getInstance().getTime());
      jLabel_date.setText(today);
      
        String query= "SELECT * FROM Inventory where  -7<=DATEDIFF(day,EXP_Date,'"+today+"') and Qty>0 ";//get all details            
            st = con.createStatement();
            rs= st.executeQuery(query);
            User user;
            while(rs.next()){
            user = new User(rs.getString("Item"),rs.getInt("Item_Code"),rs.getDate("EXP_Date"),rs.getFloat("Qty"),rs.getFloat("Total_Cost"));
            userList.add(user);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
        return userList;
    }
       
       
