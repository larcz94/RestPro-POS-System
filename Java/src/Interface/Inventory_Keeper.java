    public void search() {

        Connection con = DBconnect.connectdb();
        Statement st;
        ResultSet rs;

        try {

            String query = "SELECT Item,Qty,EXP_Date,Unit_Price FROM Inventory where Item_Code='" + jTextField_Item_code.getText() + "' ";//get all details            
            st = con.createStatement();
            rs = st.executeQuery(query);

            if (rs.next()) {

                Name.setText(rs.getString(1));//name of item
                Available.setText("" + rs.getFloat(2));//available units
                EXP.setText("" + rs.getDate(3));//exp date of item
                Unit_price = rs.getFloat(4);//unit price 
                jLabel_item_name.setText(rs.getString(1));
                

            } else {
                JOptionPane.showMessageDialog(rootPane, "Invalied Item Code");

            }
        } catch (Exception e) {

            e.printStackTrace();
        }

    }


