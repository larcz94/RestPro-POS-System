    public void update(int value) {

        float qty = Float.parseFloat(jTextField_qty.getText());// user enterd qty
        float available = Float.parseFloat(Available.getText());// available qty

        float newQty1 = available + qty; // if enter new item => new qty
        float newQty2 = available - qty;//if out some item => new qty

        if (value == 1) {//value 1 is new item entered

            TrueQty = newQty1;
            cost = Unit_price * TrueQty;
        } else {//value 2 is  item out
            TrueQty = newQty2;
            cost = Unit_price * TrueQty;
        }

        String query = " UPDATE Inventory SET Qty='" + TrueQty + "' , Total_Cost='" + cost + "'  WHERE Item_Code='" + jTextField_Item_code.getText() + "' ";
        if (value == 2) {//out items
            if (qty <= available) {//check available and demond

                Statement st;
                Connection con = DBconnect.connectdb();

                try {
                    st = con.createStatement();
                    st.executeUpdate(query);

                    JOptionPane.showMessageDialog(rootPane, "Update Successful");
                   this.dispose();
                   new Inventory_Keeper(userid, RealName).setVisible(true);

                } catch (Exception e) {
                    JOptionPane.showMessageDialog(rootPane, e);
                }

            } else {//can not give items Insufficient items

                JOptionPane.showMessageDialog(rootPane, "Insufficient Units!  " + available+ " Units Are Available");

            }
        } else {//enter items
            Statement st;
            Connection con = DBconnect.connectdb();

            try {
                st = con.createStatement();
                st.executeUpdate(query);

                JOptionPane.showMessageDialog(rootPane, "Update Successful");
                this.dispose();
                   new Inventory_Keeper(userid, RealName).setVisible(true);

            } catch (Exception e) {
                JOptionPane.showMessageDialog(rootPane, e);
            }
        }

    }

