    private int Auto_Employee_Id() {

        int number = 1;

        try {
            String sql = "Select max(Emp_Id) from Employee";

            PreparedStatement pst = DBconnect.connectdb().prepareStatement(sql);
            ResultSet rs = pst.executeQuery();

            if (rs.next()) {
                number = rs.getInt(1);
            }
            number++;

        } catch (SQLException e) {
        }
        return number;
    }

