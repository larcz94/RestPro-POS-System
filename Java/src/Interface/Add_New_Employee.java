    public void executeQuery(String query, int a) {

        Connection con = DBconnect.connectdb();
        Statement st;

        try {
            st = con.createStatement();
            st.executeUpdate(query);
            if (a == 1) {
                JOptionPane.showMessageDialog(rootPane, "Employee Adding Successfully!");
                this.dispose();
                new Add_New_Employee(RealName).setVisible(true);
                clear();
                jTextField_Name.requestFocus();
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);

        }

    }

