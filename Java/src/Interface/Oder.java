    public void productSelect() {
        String sql = "SELECT Product_Name,Qty,Selling_Price FROM Product where Product_Code='" + jTextField_Product_Code.getText() + "' ";

        try {

            PreparedStatement pst = DBconnect.connectdb().prepareStatement(sql);
            ResultSet rs = pst.executeQuery();
            if (rs.next()) {

                jTextField_Product.setText(rs.getString(1));
                jLabel_price.setText("" + rs.getFloat(3));
                jTextField_Qty.requestFocus();

            }
            pst.close();
            rs.close();
        } catch (SQLException e) {
            System.out.println(e);
        }
    }

