    public ArrayList<Selling_Details> getuserList() {

        ArrayList<Selling_Details> userList = new ArrayList<Selling_Details>();
        Connection connection = DBconnect.connectdb();

        String query = " select Product_Name,Size,Qty from Orders  where Table_No='" + jTextField_Table_No.getText() + "' ";
        Statement st;
        ResultSet rs;

        try {
            st = connection.createStatement();
            rs = st.executeQuery(query);
            Selling_Details user;
            while (rs.next()) {
                user = new Selling_Details(rs.getString("Product_Name"), rs.getString("Size"), rs.getFloat("Qty"));
                userList.add(user);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
        return userList;
    }

