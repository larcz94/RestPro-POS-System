public void autofill(){
int itemCode=Integer.parseInt(jTextField_Item_Code.getText());


       String sql = "SELECT i.Item,i.Supplier_Id,i.Qty,s.Name FROM Inventory i,Supplier s where Item_Code='" + itemCode + "' and i.Supplier_Id=s.Supplier_Id";

        try {

            PreparedStatement pst = DBconnect.connectdb().prepareStatement(sql);
            ResultSet rs = pst.executeQuery();
            if (rs.next()) {

                jTextField_Product_Name.setText(rs.getString(1));
                int S_Id = rs.getInt(2);//get Catogory name
                jTextField_s_id.setText("" + S_Id);
                qty = rs.getFloat(3);//get Qty
                jLabel_Available.setText("" + qty);
                qty = rs.getFloat(3);//get supplier name
                
                jLabel11_suplier_id.setText(rs.getString(4));

            }
            pst.close();
            rs.close();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(rootPane, "Error INPUT");
        }


}



