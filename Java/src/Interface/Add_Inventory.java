    private int Auto_Product_Code() {

        int number = 1000;

        try {
            String sql = "Select max(Item_Code) from Inventory";

            PreparedStatement pst = DBconnect.connectdb().prepareStatement(sql);
            ResultSet rs = pst.executeQuery();

            if (rs.next()) {
                number = rs.getInt(1);

            }
            New_Product = true;
            number++;

            jTextField_Item_Code.setText("" + number);
            jTextField_s_id.requestFocus();
        } catch (SQLException e) {
        }
        return number;
    }

