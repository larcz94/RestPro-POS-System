       public void Show_jTable(){
    int count=0;
    float Total=0;
    float discount=0;
    
        ArrayList<User> list = getuserList();
        DefaultTableModel model =(DefaultTableModel)jTable1.getModel();
        Object [] row = new Object[4];
        for (int i=0; i<list.size(); i++){
            row[0]=list.get(i).getDate();
            row[1]=list.get(i).getHours();
            row[2]=list.get(i).getSelling_Price();
            row[3]=list.get(i).getQty();
            
            Total=Total+list.get(i).getSelling_Price();
            count++;
            discount=discount+list.get(i).getQty();
            model.addRow(row);
            
           } 
        jLabel_No_of_Sellings.setText(Integer.toString(count));
        jLabel_Total.setText(Float.toString(Total));
        jLabel_discount.setText(""+discount);
      
    }
       
       
       
