       public ArrayList <User> getuserList(){
    
        ArrayList<User> userList = new ArrayList<User>();
        Connection con = DBconnect.connectdb();
        
        Statement st;
        ResultSet rs;
        
        try {
            
            
             SimpleDateFormat dfomat = new SimpleDateFormat("yyyy-MM-dd");
        String date = dfomat.format(jDateChooser1.getDate());
            jLabel_Date.setText(date);
          
       
      
        String query= "SELECT * FROM Selling WHERE Date='"+date+"' ";
            
            st = con.createStatement();
            rs= st.executeQuery(query);
            User user;
            while(rs.next()){
            user = new User(rs.getDate("Date"),rs.getInt("Bill_No"),rs.getFloat("Total_Amount"),rs.getFloat("Discount"));
            userList.add(user);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
        return userList;
    }
       
       
