   private int Auto_Membership_Card_no() {

        int number = 1;

        try {
            String sql = "Select max(Member_Id) from Member";

            PreparedStatement pst = DBconnect.connectdb().prepareStatement(sql);
            ResultSet rs = pst.executeQuery();

            if (rs.next()) {
                number = rs.getInt(1);
            }
            number++;

        } catch (SQLException e) {
        }
        return number;
    }
   
