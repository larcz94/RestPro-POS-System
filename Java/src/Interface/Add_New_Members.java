        public ArrayList <User> getuserList(){
    
        ArrayList<User> userList = new ArrayList<User>();
        Connection connection = DBconnect.connectdb();
        
        String query= "SELECT * FROM User_Filled_Details ";
        Statement st;
        ResultSet rs;
        
        try {
            st = connection.createStatement();
            rs= st.executeQuery(query);
            User user;
            while(rs.next()){
            user = new User(rs.getString("First_Name"),rs.getString("Last_Name"),rs.getString("Nic"),rs.getString("Gender"),rs.getInt("Contact_No"),rs.getString("Address"),rs.getString("Email"));
            userList.add(user);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
        return userList;
    }
    
