        public ArrayList <User> getuserList(){
    
        ArrayList<User> userList = new ArrayList<User>();
        Connection con = DBconnect.connectdb();
        
        Statement st;
        ResultSet rs;
        
        try {
          
         SimpleDateFormat dfomat = new SimpleDateFormat("yyyy-MM-dd");
    String date = dfomat.format(jDateChooser1.getDate());
          date1=date;
    String query = "SELECT w.Emp_Id,w.Amount,e.Employee_Name FROM Working_Details w, Employee e where Date='"+date+"' and w.Emp_Id=e.Emp_Id ";
        
      
     
            st = con.createStatement();
            rs= st.executeQuery(query);
            User user;
            while(rs.next()){
            user = new User(rs.getString(3),rs.getFloat(2));
            userList.add(user);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
        return userList;
        
        
    }
       
       
