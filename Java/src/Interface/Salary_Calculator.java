       public void Show_jTable(){
    int count=0;
    float total_amount=0;
        ArrayList<User> list = getuserList();
        DefaultTableModel model =(DefaultTableModel)jTable1.getModel();
        Object [] row = new Object[2];
        for (int i=0; i<list.size(); i++){
          
            row[0]=list.get(i).getFullName();
            row[1]=list.get(i).getAmount();
            
            total_amount=total_amount+list.get(i).getAmount();//calculate total salary
            count++;
            model.addRow(row);
            
           } 
        jLabel_No_of_Emp.setText(Integer.toString(count));
        jLabel_total_salary.setText(Float.toString(total_amount));
        jLabel_Date.setText(date1);
    }
       
    
    
