     public String Getgender() {
        String gender = null;
        if (jRadioButton1.isSelected()) {
            gender = "Male";
        } else if (jRadioButton2.isSelected()) {
            gender = "Female";
        } else {
            gender = "Not Select";
        }

        return gender;
    }

    
