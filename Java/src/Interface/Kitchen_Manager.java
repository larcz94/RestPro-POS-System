     public void executeQuery(String query) {

        Connection con = DBconnect.connectdb();
        Statement st;

        try {
            st = con.createStatement();
            st.executeUpdate(query);

            DefaultTableModel model = (DefaultTableModel) jTable_out.getModel();
            model.setRowCount(0);
           
          Show_jTable1();
           Show_jTable();
           this.dispose();
           new Kitchen_Manager(Emp_ID,RealName).setVisible(true);

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);

        }

    }
    
    
