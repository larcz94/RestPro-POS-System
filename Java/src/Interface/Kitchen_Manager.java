    public ArrayList<Selling_Details> getuserList() {

        ArrayList<Selling_Details> userList = new ArrayList<Selling_Details>();
        Connection connection = DBconnect.connectdb();

        String query = " select Table_No,Emp_Id,Product_Name,Size,Qty from Orders where Status = 0   ";
        Statement st;
        ResultSet rs;

        try {
            st = connection.createStatement();
            rs = st.executeQuery(query);
            Selling_Details user;
            while (rs.next()) {
                user = new Selling_Details(rs.getInt("Table_No"),rs.getInt("Emp_Id"),rs.getString("Product_Name"), rs.getString("Size"), rs.getFloat("Qty"));
                userList.add(user);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
        return userList;
    }

    //Display data
    public void Show_jTable() {

        ArrayList<Selling_Details> list = getuserList();
        DefaultTableModel model = (DefaultTableModel) jTable_In.getModel();

        Object[] row = new Object[5];
        for (int i = 0; i < list.size(); i++) {
            row[0] = list.get(i).getTable_no();
            row[1] = list.get(i).getEmp_id();
            row[2] = list.get(i).getItem();
            row[3] = list.get(i).getSize();
            row[4] = list.get(i).getQty();

            model.addRow(row);

        }

    }

   
