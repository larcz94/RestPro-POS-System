    public void autofill() {

        String sql = "SELECT Product_Name,Category_Name,Qty FROM Product where Product_Code='" + jTextField_Item_Code.getText() + "' ";

        try {

            PreparedStatement pst = DBconnect.connectdb().prepareStatement(sql);
            ResultSet rs = pst.executeQuery();
            if (rs.next()) {

                jTextField_Product_Name.setText(rs.getString(1));
                String name = rs.getString(2);//get Catogory name
                qty = rs.getFloat(3);//get Qty
                jLabel_Available.setText("" + qty);

                if ("Breakfist".equals(name)) {
                    jComboBox_Catogory.setSelectedIndex(0);
                } else if ("Lunch".equals(name)) {
                    jComboBox_Catogory.setSelectedIndex(1);
                } else if ("Dinner".equals(name)) {
                    jComboBox_Catogory.setSelectedIndex(2);
                } else if ("Salad".equals(name)) {
                    jComboBox_Catogory.setSelectedIndex(3);
                } else {
                    jComboBox_Catogory.setSelectedIndex(4);
                }

            }
            pst.close();
            rs.close();
        } catch (SQLException e) {
            System.out.println(e);
        }

    }

