    private int Auto_Product_Code() {

        int number = 1000;

        try {
            String sql = "Select max(Product_Code) from Product";

            PreparedStatement pst = DBconnect.connectdb().prepareStatement(sql);
            ResultSet rs = pst.executeQuery();

            if (rs.next()) {
                number = rs.getInt(1);

            }
            New_Product = true;
            number++;

            jTextField_Item_Code.setText("" + number);
            jTextField_Product_Name.requestFocus();
        } catch (SQLException e) {
        }
        return number;
    }

